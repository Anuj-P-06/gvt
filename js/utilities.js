export function prefersReducedMotion() {
  return window.matchMedia('(prefers-reduced-motion: reduce)').matches
}

/** Crossfade: fade out old, swap callback, fade in new. */
export function crossfadeSwap(container, renderFn) {
  const current = container.firstElementChild
  if (!current) {
    container.appendChild(renderFn())
    return
  }

  if (prefersReducedMotion()) {
    container.replaceChildren(renderFn())
    return
  }

  current.classList.add('crossfade-exit')
  current.classList.remove('crossfade-active')

  const onDone = () => {
    current.removeEventListener('transitionend', onDone)
    const next = renderFn()
    next.classList.add('crossfade-enter')
    container.replaceChildren(next)
    requestAnimationFrame(() => {
      next.classList.remove('crossfade-enter')
      next.classList.add('crossfade-active')
    })
  }

  current.addEventListener('transitionend', onDone)
  setTimeout(onDone, 450)
}

export function createEl(html) {
  const tpl = document.createElement('template')
  tpl.innerHTML = html.trim()
  return tpl.content.firstElementChild
}
